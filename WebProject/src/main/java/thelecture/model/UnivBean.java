package thelecture.model;

public class UnivBean{
	private String univ_name;
	private String univ_url;
	private String univ_domain;
	private String univ_logo;
	private String uploadedfile; // univ -->업로드 이미지 파일
	
	public String getUniv_name() {
		return univ_name;
	}
	public void setUniv_name(String univ_name) {
		this.univ_name = univ_name;
	}
	public String getUniv_url() {
		return univ_url;
	}
	public void setUniv_url(String univ_url) {
		this.univ_url = univ_url;
	}
	public String getUniv_domain() {
		return univ_domain;
	}
	public void setUniv_domain(String univ_domain) {
		this.univ_domain = univ_domain;
	}
	public String getUniv_logo() {
		return univ_logo;
	}
	public void setUniv_logo(String univ_logo) {
		this.univ_logo = univ_logo;
	}
	public String getUploadedfile() {
		return uploadedfile;
	}
	public void setUploadedfile(String uploadedfile) {
		this.uploadedfile = uploadedfile;
	}

	
}
