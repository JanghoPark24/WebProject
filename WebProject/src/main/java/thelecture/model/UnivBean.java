package thelecture.model;

public class UnivBean {
	private String Univ_name;
	private String Univ_domain;
	private String Univ_logo;
	public String getUniv_name() {
		return Univ_name;
	}
	public void setUniv_name(String univ_name) {
		Univ_name = univ_name;
	}
	public String getUniv_domain() {
		return Univ_domain;
	}
	public void setUniv_domain(String univ_domain) {
		Univ_domain = univ_domain;
	}
	public String getUniv_logo() {
		return Univ_logo;
	}
	public void setUniv_logo(String univ_logo) {
		Univ_logo = univ_logo;
	}
	
}
